#include <unistd.h>

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*p;
	unsigned char	t;

	p = (unsigned char *)s;
	t = (unsigned char)c;
	while (n--)
		p[n] = t;
	return (s);
}
