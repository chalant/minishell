#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t				i;
	const unsigned char	*sc;
	unsigned char		cc;

	sc = (const unsigned char *)s;
	cc = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if (sc[i] == cc)
			return ((void *)(s + i));
		i++;
	}
	return (NULL);
}
